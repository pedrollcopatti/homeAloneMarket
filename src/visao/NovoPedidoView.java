    private void jlAdicionaProdutoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlAdicionaProdutoMouseClicked
        Produto produto = (Produto) cbProduto.getSelectedItem();
        if (produto == null){
            JOptionPane.showMessageDialog(null, "Nenhum produto selecionado!");
        } else {
            String listaProduto = taProdutosLista.getText();
            listaProduto = listaProduto + produto.imprimeProduto() + System.lineSeparator();
            taProdutosLista.setText(listaProduto);
            this.produtosCompra.add(produto);
        }
    }//GEN-LAST:event_jlAdicionaProdutoMouseClicked

