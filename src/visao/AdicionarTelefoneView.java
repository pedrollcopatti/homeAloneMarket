    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        String ddd = tfDdd.getText();
        String telefone = tfTelefone.getText();
        ArrayList<Telefone> telefones = homeAloneView.getTelefones();
        boolean existe = false;
        
        for (Telefone telefoneFor : telefones) {
            if (telefoneFor.getDdd().equals(ddd) 
                && telefoneFor.getTelefone().equals(telefone)){
                existe = true;
                break;
            }
        }
        if (existe){
            JOptionPane.showMessageDialog(null, "Telefone j√° cadastrado");
        } else {
            telefones.add(new Telefone(ddd, telefone));
            JOptionPane.showMessageDialog(null, "Telefone cadastrado com sucesso!");
        }
        
        
    }//GEN-LAST:event_btSalvarActionPerformed

