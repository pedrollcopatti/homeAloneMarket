    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        ArrayList<Compra> compras = homeAloneView.getCompras();
        
        String newTelefone = ((cbTelefone.getSelectedItem() == null) ? "" : cbTelefone.getSelectedItem().toString()),
               newEndereco = ((cbEndereco.getSelectedItem() == null) ? "" : cbEndereco.getSelectedItem().toString()),
               newEmail = ((cbEmail.getSelectedItem() == null) ? "" : cbEmail.getSelectedItem().toString()),
               newPagamento = ((cbPagamento.getSelectedItem() == null) ? "" : cbPagamento.getSelectedItem().toString());
        
        Compra compra = new Compra(tfDataPrevisao.getText(), 
                                   newTelefone, 
                                   newEndereco,
                                   newEmail,
                                   newPagamento);
        compra.adicionaProduto(this.produtosCompra);
        compras.add(compra);
        this.setVisible(false);
        homeAloneView.setVisible(true);
    }//GEN-LAST:event_btSalvarActionPerformed

