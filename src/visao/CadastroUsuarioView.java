    private void btConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmarActionPerformed
        String nome = tfNome.getText();
        String cpfCnpjText = tfCpfCnpj.getText();
        cpfCnpjText = cpfCnpjText.replaceAll("[^0-9]", "");
        String senha = pfSenha.getText();
        boolean isCadastrado = false;
        boolean valido = cpfCnpjText.matches("([0-9]{2}[\\.]?[0-9]{3}[\\.]?[0-9]{3}[\\/]?[0-9]{4}[-]?[0-9]{2})|([0-9]{3}[\\.]?[0-9]{3}[\\.]?[0-9]{3}[-]?[0-9]{2})");
        long cpfCnpj = 0;
        if (valido)
            cpfCnpj = Long.parseLong(cpfCnpjText);
        
        if (!(cpfCnpj > 0 && valido)) {
            JOptionPane.showMessageDialog(null, "CPF/CNPJ Inv치lido!");
        } else {
            for (Usuario usuario : usuarios) {
                if (cpfCnpj == usuario.getCpfCpnj()){
                    isCadastrado = true;
                    break;
                }
            }
            if (isCadastrado == false) {
                usuarios.add(new Usuario(nome, cpfCnpj, senha));
                JOptionPane.showMessageDialog(null, "Usu치rio inserido com sucesso!");
                this.dispose();
                this.login.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Usu치rio j치 cadastrado!");
            }
        }
    }//GEN-LAST:event_btConfirmarActionPerformed

